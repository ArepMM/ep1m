void ShuntsModule::load_config(CfgReader &cfg)
{
    QString secName = "Device";

    for (size_t i = 0; i < beta.size(); ++i)
    {
        QString paramName = QString("Step%1").arg(i);
        cfg.getDouble(secName, paramName, beta[i]);
    }
}
